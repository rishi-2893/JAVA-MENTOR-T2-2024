// Conditional Loops Example
public class ConditionalLoopsExample {
    public static void main(String[] args) {
        int i = 1;

        while (i <= 5) {
            System.out.println("Count: " + i);
            i++;
        }

        for (int j = 1; j <= 5; j++) {
            System.out.println("Count: " + j);
        }
    }
}
